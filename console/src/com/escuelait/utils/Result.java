package com.escuelait.utils;

public class Result {
	
	private int whites;
	private int blacks;
	
	public Result() {}
	
	public Result(int whites, int blacks) {
		this.whites = whites;
		this.blacks = blacks;
	}
	
	public int getWhites() {
		return whites;
	}
	public void setWhites(int whites) {
		this.whites = whites;
	}
	public int getBlacks() {
		return blacks;
	}
	public void setBlacks(int blacks) {
		this.blacks = blacks;
	}
	
	
}
